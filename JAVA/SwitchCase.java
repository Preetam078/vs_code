public class SwitchCase {
    
    public static void main(String[] args) {
        
        char grade = 'A';
        
        switch (grade) {
            case 'c':
                System.out.println("exellent");
                break;
            case 'B':
                System.out.println("very good");
                break;
            default:
            System.out.println("mar ja");
                break;
        }
    }
}
